		string := "\xe2\x80\x9c\x6e\x6f\x72\x64\x20\x6f\x67\x20\x73\xc3\xb8\x72\xe2\x80\x9d\x20\x70\xc3\xa5\x20\x6a\x61\x70\x61\x6e\x73\x6b\x20\x65\x72\x20\xe2\x80\x9c\xe5\x8c\x97\xe3\x81\xa8\xe5\x8d\x97\xe2\x80\x9d"
